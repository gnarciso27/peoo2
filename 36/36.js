function retornarMaiorNumero(numero1, numero2) {
    if (numero1 > numero2) {
      return numero1;
    } else {
      return numero2;
    }
  }
  
 
  console.log(retornarMaiorNumero(10, 20)); 
  console.log(retornarMaiorNumero(30, 15)); 
  console.log(retornarMaiorNumero(25, 25));  
  console.log(retornarMaiorNumero(-5, -10));